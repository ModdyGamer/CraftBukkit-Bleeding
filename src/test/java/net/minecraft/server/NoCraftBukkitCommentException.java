package net.minecraft.server;

public class NoCraftBukkitCommentException extends CommentException {

    public NoCraftBukkitCommentException() {
        super();
    }

    public NoCraftBukkitCommentException(String message, Throwable cause) {
        super(message, cause);
    }

    public NoCraftBukkitCommentException(String message) {
        super(message);
    }

    public NoCraftBukkitCommentException(Throwable cause) {
        super(cause);
    }
}

