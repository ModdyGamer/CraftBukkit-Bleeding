package net.minecraft.server;

public class IllegalBukkitCallException extends CommentException {

    public IllegalBukkitCallException() {
        super();
    }

    public IllegalBukkitCallException(String message, Throwable cause) {
        super(message, cause);
    }

    public IllegalBukkitCallException(String message) {
        super(message);
    }

    public IllegalBukkitCallException(Throwable cause) {
        super(cause);
    }
}

